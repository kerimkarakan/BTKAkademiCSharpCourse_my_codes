for(int i = 1; i <= 20; i += 2){
    Console.Write($"{i}, ");
}
Console.WriteLine();

// while ile aynı işlem
int j = 1;
while(j <= 20){
    Console.Write($"{j}, ");
    j +=2;
}
Console.WriteLine(); // boşluk bırakmak için

// do-while ile aynı işlem
int k = 1;
do{
    Console.Write($"{k}, ");
    k += 2;
} while(k <= 20);





